public class StringExercise {
    public static void main(String[] args){
        System.out.println("Hello, welcome to Strings Exercise!");

        String str1 = "We dont need no education.";
        System.out.println(str1);
        System.out.println();
        String str2 = "We dont need no thought control.";
        System.out.println(str2);
        System.out.println();
        String str3 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(str3);
        System.out.println();
        String str4 = "In windows, the main drive is usually C:\\";
        System.out.println(str4);
        System.out.println();
        String str5 = "I can do backslashes \\, double backslashes \\\\,\n and the amazing triple backslash \\\\\\!";
        System.out.println(str5);
    }
}
